    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String q="";
        if(x==1)
            q="Delete from player where id="+jTextField1.getText();
        else if(x==2)
            q="Delete from batting_record where id="+jTextField1.getText();
        else if(x==3)
            q="Delete from bowling_record where id="+jTextField1.getText();
        else if(x==4)
            q="Delete from fielding_record where id="+jTextField1.getText();
        else if(x==5)
            q="Delete from keeping_record where id="+jTextField1.getText();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/ccm", "root", "nigger");
            Statement st = con.createStatement();
            st.executeUpdate(q);
            con.close();
        }
        catch(Exception e) {
            y=2;
            if(x==0)
                JOptionPane.showMessageDialog(null,"Select Something");
            else
                if(x!=1)
                    JOptionPane.showMessageDialog(null,"Something went wrong!!");
        }
        if(y==1) {
            if(x==1) {
                p.setVisible(true);
                dispose();
            }
            else if(x==2) {
                bats.setVisible(true);
                dispose();
            }
            else if(x==3) {
                bo.setVisible(true);
                dispose();
            }
            else if(x==4) {
                f.setVisible(true);
                dispose();
            }
            else if(x==5) {
                k.setVisible(true);
                dispose();
            }
            else {
                JOptionPane.showMessageDialog(null,"Select an option");
            }
        }
        else {
            if(x==1)
                JOptionPane.showMessageDialog(null,"Cannot delete the Parent Row \n     Delete Child Rows first! ");            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

